package cleancode;

public class materials {
	public materials(){
		
	}
	public double sm(double feet){
		return feet*1200;
	}
	public double ssm(double feet){
		return feet*1500;
	}
	public double hsm(double feet){
		return feet*1800;
	}
	public double hfsm(double feet){
		return feet*2500;
	}
	

}
